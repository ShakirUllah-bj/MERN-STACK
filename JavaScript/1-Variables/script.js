// What is Variable?
// A Variable is a container for storing data values.
// The data can be changed or used throughout the program.

// How to Declare Variables:
// We can declare variables in JavaScript using three keywords:

// 1. var:
// an older way of declaring variables, now considered less 
// used due to issues like hoisting.

// Example: 
// var myName = "Shakir Ullah";
// var myAge = 20;
// var isStudent = true;

// 2. let:
//    A modern way to declare variables with block Scope.

//Example:
//let myName = "Shakir Ullah";
//let myAge = 20;
//let isStudent = true;

// 3. const:
//    used to declare variables that cannot be reassigned (constant values).

//Example:
//const myName = "Shakir Ullah";
//const birthYear = 2004;
//const isMale = true;
